namespace WowArmory.Core.Languages
{
	public class LocalizedStrings
	{
		//----------------------------------------------------------------------
		#region --- Fields ---
		//----------------------------------------------------------------------
		private static AppResources _localizedResources;
		//----------------------------------------------------------------------
		#endregion
		//----------------------------------------------------------------------


		//----------------------------------------------------------------------
		#region --- Properties ---
		//----------------------------------------------------------------------
		/// <summary>
		/// Gets the localized resources object.
		/// </summary>
		public AppResources LocalizedResources
		{
			get { return _localizedResources ?? (_localizedResources = new AppResources()); }
		}
		//----------------------------------------------------------------------
		#endregion
		//----------------------------------------------------------------------


		//----------------------------------------------------------------------
		#region --- Constructor ---
		//----------------------------------------------------------------------
		/// <summary>
		/// Initializes a new instance of the <see cref="LocalizedStrings"/> class.
		/// </summary>
		public LocalizedStrings()
		{
		}
		//----------------------------------------------------------------------
		#endregion
		//----------------------------------------------------------------------
	}
}